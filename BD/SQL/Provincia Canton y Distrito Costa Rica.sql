CREATE SCHEMA IF NOT EXISTS `Localidades` DEFAULT CHARACTER SET utf8 ;
USE `Localidades` ;

CREATE TABLE IF NOT EXISTS  provincias(
Cod char(1) NOT NULL,
Nombre varchar(50) NOT NULL,
PRIMARY KEY (Cod),
UNIQUE INDEX `provincias_Codigo_UNIQUE` (Cod ASC))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8;

CREATE TABLE IF NOT EXISTS  cantones(
provincias char(1),
cantones char(2),
primary key(provincias,cantones),
Nombre varchar(50),
CONSTRAINT `fk_cantones_provincias`
    FOREIGN KEY (provincias)
    REFERENCES provincias (Cod)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION)
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8;

CREATE TABLE IF NOT EXISTS distritos(
provincias char(1),
cantones char(2),
distritos char(2),
primary key(provincias,cantones,distritos),
Nombre varchar(50),
CONSTRAINT `fk_distritos_provincias`
    FOREIGN KEY (provincias)
    REFERENCES provincias (Cod)
     ON DELETE NO ACTION
    ON UPDATE NO ACTION,
CONSTRAINT `fk_distritos_cantones`
    FOREIGN KEY (cantones)
    REFERENCES cantones (cantones)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION)
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8;

